# Booleanos são tipos primitivos que representam decisões (sim ou não)
# São representados por True (verdadeiro) ou False (false)
# Exemplo:
print(True)
print(False)
print(type(True))
print(type(False))
print(10 == 10)
print(10 == 11)

# A função type retorna o tipo to valor (se ele é int, float, string, bool, etc)
# == é um sinal de comparação. Neste caso, 10 é igual 10? e o retorno será True