# Função print serve para mostrar (imprimir) coisas na tela, recebendo essas 'coisas' como argumento
# Exemplo:
print(123) # 123 são argumentos para o print

# A função pode tomar mais de 1 argumento
# Exemplo:

print(12, 34) # necessário usar a vírgula 
print("a" + "b") # ou o + (para concatenar as frases, no caso de strings)

# Em Python, há argumentos não nomeados (como os descritos anteriormente) e argumentos nomeados
# Se deseja mudar o separador do print, pode-se escrever:
print(12, 34, sep="") # o separador seria nenhum valor

# Pode-se mudar o valor final, também, utilizando o argumento 'end='. Pode colocar qualquer valor
print(12, 34, end="") # agora o código não irá pular linha automaticamente
print("a" + "b") 
